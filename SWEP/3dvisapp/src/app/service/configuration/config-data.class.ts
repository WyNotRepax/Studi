/**
 * Helper class to map keyboard inputs from config file
 */
export class ConfigData {
  /**
   * Left control key
   */
  left: string;
  /**
   * Right control key
   */
  right: string;
  /**
   * Up control key
   */
  up: string;
  /**
   * Down control key
   */
  down: string;
  /**
   * Zoom in key
   */
  zoomin: string;
  /**
   * Zoom out key
   */
  zoomout: string
}
